package com.schedulingapp.misc;

/**
 * An Enumeration of all possible values for Gender we are considering relevant for the
 * purposes of our app. Non-Binary genders are not considered as this data is being kept
 * to ensure that a shift complies with the laws that at least one male and female staff
 * are present during any one shift.
 *
 * @author Benjamin Corder
 * @version 1.0
 * @since 1.0
 */
public enum Gender {
    GENDER_UNSPECIFIED,
    MALE,
    FEMALE
}
